package isistan.soploon.project;

public class JavaFile {
	
	private String path;
	private String code;
	
	public JavaFile () {
		
	}
	
	public String getCode() {
		return this.code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getPath() {
		return path;
	}
	
	public void setPath(String path) {
		this.path = path;
	}
	

}
